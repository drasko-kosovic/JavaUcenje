package com.company;

public class Dimenzije {
    private int duzina;
    private int sirina;

    public Dimenzije(int duzina, int sirina) {
        this.duzina = duzina;
        this.sirina = sirina;
    }

    public int getDuzina() {
        return duzina;
    }

    public int getSirina() {
        return sirina;
    }
}
